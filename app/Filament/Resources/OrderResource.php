    {
        return $infolist
            ->schema([
                Section::make('Order info')
                    ->schema([
                        TextEntry::make('order_date')->label('Order Date'),
                        TextEntry::make('customer.name')->label('Customer'),
                        TextEntry::make('stockItem.name')->label('Stock Item'),
                        TextEntry::make('quantity')->label('Quantity'),
                        TextEntry::make('size.name')->label('Size'),
                        TextEntry::make('PaperVendor.name')->label('Paper'),
                        TextEntry::make('plateVendor.name')->label('Plate'),
                        TextEntry::make('printingVendor.name')->label('Printing'),
                        TextEntry::make('laminationVendor.name')->label('Lamination'),
                        TextEntry::make('bindingVendor.name')->label('Binding'),
                        TextEntry::make('delivery_date')->label('Delivery Date'),
                        TextEntry::make('amount')->label('Amount'),
                    ])->columns(4)
            ]);

    }

